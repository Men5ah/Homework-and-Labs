public class Thing {
    private String name;
    private String color;

    // Getters
    public String getName() {
        return name;
    }
    public String getColor() {
        return color;
    }

    // Setters
    public void setName(String name) {
        this.name = name;
    }
    public void setColor(String color) {
        this.color = color;
    }

    // Constructor
    public Thing(String name, String color) {
        this.name = name;
        this.color = color;
    }

    

}
